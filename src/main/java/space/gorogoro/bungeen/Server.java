package space.gorogoro.bungeen;

public class Server {
  
  public String world;
  public Integer x;
  public Integer y;
  public Integer z;
  public String ip;
  public Integer port;
  public String server;
  public String type;
  public String name;
  public String comment;
  public Integer fail;
  public Long failtimestamp;
  
  public String getWorld() {
    return world;
  }
  public void setWorld(String str) {
    world = str;
  }
  public Integer getX() {
    return x;
  }
  public void setX(Integer i) {
    x = i;
  }
  public Integer getY() {
    return y;
  }
  public void setY(Integer i) {
    y = i;
  }
  public Integer getZ() {
    return z;
  }
  public void setZ(Integer i) {
    z = i;
  }
  public String getIp() {
    return ip;
  }
  public void setIp(String str) {
    ip = str;
  }
  public Integer getPort() {
    return port;
  }
  public void setPort(Integer i) {
    port = i;
  }
  public String getServer() {
    return server;
  }
  public void setServer(String str) {
    server = str;
  }
  public String getType() {
    return type;
  }
  public void setType(String str) {
    type = str;
  }
  public String getName() {
    return name;
  }
  public void setName(String str) {
    name = str;
  }
  public String getComment() {
    return comment;
  }
  public void setComment(String str) {
    comment = str;
  }
  public Integer getFail() {
    return fail;
  }
  public void setFail(Integer i) {
    fail = i;
  }
  public Long getFailTimestamp() {
    return failtimestamp;
  }
  public void setFailTimestamp(Long i) {
    failtimestamp = i;
  }
}
